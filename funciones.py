num = 10
nums = 100

def suma(num1, num2):
    
    print(num1 + num2)
    print("Estoy sumando en una funcion")
    
suma(num, nums)

def resta(num1, num2):
    
    print(num1 - num2)
    print("Estoy restando en una funcion")
    
resta(num, nums)

def multiplicacion(num1, num2):
    
    print(num1 * num2)
    print("Estoy multiplicando en una funcion")
    
multiplicacion(num, nums)

def division(num1, num2):
    
    print(num1 / num2)
    print("Estoy dividiendo en una funcion")
    
division(num, nums)
